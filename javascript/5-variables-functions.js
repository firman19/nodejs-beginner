// A variable is a container for a value, like a number we might use in a sum, or a string that we might use as part of a sentence.

// To use a variable, you've first got to create it — more accurately,
//  we call this declaring the variable.
//  To do this, we type the keyword let followed by
//  the name you want to call your variable:

let myName;
let myAge;

// 2. Functions

// Functions are one of the fundamental building blocks in JavaScript.
// A function in JavaScript is a set of statements that performs a task or calculates a value.
// It should take some input and return an output

// The name of the function.
// A list of parameters to the function, enclosed in parentheses and separated by commas.
// The JavaScript statements that define the function, enclosed in curly brackets, { /* … */ }.

// The function square takes one parameter, called number
function square(number) {
  // The statement return specifies the value returned by the function:
  return number * number;
}
